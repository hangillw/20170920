package study;

public class Ex09_Variable07 {
	public static void main(String[] args){
		/*
		  정수와 실수 값을 사용한 산술 연산
		  byte + byte ==> int
		  short + byte ==> int
		  int + int ==> int
		  byte + long ==> long
		  int + float ==> float
		  int + double ==> double
		  
		 */
		
		// () <-- 형변환(casting) 연산자
		// 		   명시적 형변환 = 강제 형변환
		int number;
		
//		number = 10 + 2.5F;
		number = (int)(10 + 2.5F);
		float flt1;
		flt1 = 10 + 2.5F;
		
		
		
	}
}
